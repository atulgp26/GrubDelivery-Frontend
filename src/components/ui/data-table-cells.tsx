export interface CheckboxCellProps {
	checked: boolean | "indeterminate";
	onChange: () => void;
	className?: string;
	isHeader?: boolean;
}

const CheckSvg = () => (
	<svg width="14" height="14" viewBox="0 0 14 14" fill="none" xmlns="http://www.w3.org/2000/svg">
		<path d="M12.4911 3.50074L5.24944 10.7424L1.50781 7.00074L2.33277 6.17578L5.24944 9.09245L11.6661 2.67578L12.4911 3.50074Z" fill="white" />
	</svg>
);

const MinusSvg = () => (
	<svg width="14" height="14" viewBox="0 0 14 14" fill="none" xmlns="http://www.w3.org/2000/svg">
		<path d="M2.33398 6.41797H11.6673V7.58464H2.33398V6.41797Z" fill="white" />
	</svg>
);

export function CheckboxCell({ checked, onChange, className, isHeader }: CheckboxCellProps) {
	const isChecked = checked === true;
	const isIndeterminate = checked === "indeterminate";

	return (
		<button
			type="button"
			role="checkbox"
			aria-checked={isIndeterminate ? "mixed" : isChecked}
			onClick={(e) => {
				e.stopPropagation();
				onChange();
			}}
			className={cn(
				"flex items-center justify-center min-w-[20px] min-h-[20px] rounded-sm border cursor-pointer transition-colors shrink-0",
				isHeader ? "w-5 h-5" : "w-[20px] h-[20px]",
				(isChecked || isIndeterminate)
					? "bg-[var(--gp-color-text-neutral-tertiary)] border-[#79867E]"
					: "bg-white border-[#79867E] hover:bg-[#ECEFEC]",
				className
			)}
		>
			{isChecked && <CheckSvg />}
			{isIndeterminate && <MinusSvg />}
		</button>
	);
}

