export type BoxBadgeVariant = "connected" | "disconnected";

export interface BoxBadgeCellProps {
	variant: BoxBadgeVariant;
	count?: number;
	className?: string;
	borderColor?: string;
}

export function BoxBadgeCell({ variant, count, className, borderColor: borderColorProp }: BoxBadgeCellProps) {
	const isConnected = variant === "connected";
	const borderColor = borderColorProp ?? (isConnected ? "#FFE3D9" : "#C1C7C4");

	return (
		<div className={cn("flex items-start justify-center pt-0.5", className)}>
			<div
				className="inline-flex items-center gap-[var(--gp-space-s)] px-[10px] py-[5px] rounded-full border bg-white"
				style={{ borderColor }}
			>
				{isConnected ? (
					<BoxOrangeIcon className="size-4 shrink-0" />
				) : (
					<BoxGrayIcon className="size-4 shrink-0" />
				)}
				{count !== undefined && (
					<span
						className="font-[var(--gp-font-text)] text-[14px] leading-[22px] text-[var(--gp-color-text-neutral-primary)]"
						style={{ fontWeight: 400 }}
					>
						{count}
					</span>
				)}
			</div>
		</div>
	);
}

